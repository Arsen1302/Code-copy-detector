class Solution:
    def solution_255_3(self, nums: List[int]) -> bool:
        # Base Condition...
        if len(nums) < 3:
            return False
        # To keep track of minimum element...
        mini = float('-inf')
        # Initialise a empty stack...
        stack = []
        # Run a Loop from last to first index...
        for i in range(len(nums)-1, -1, -1):
            # If min is greater than nums[i], return true...
            if nums[i] < mini:
                return True
            # If stack is not empty &amp;  nums[i] is greater than the top element of stack, then pop the element...
            else:
                while stack and stack[-1] < nums[i]:
                    mini = stack.pop()
            # Otherwise, append nums[i] into stack...
            stack.append(nums[i])
        # If the condition is not satisfied, return false.
        return False